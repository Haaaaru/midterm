function plus(number1, number2){
    return number1+number2;
}

function minus(number1, number2){
    return number1-number2;
}

function times(number1, number2){
    return number1*number2;
}

function div(number1,number2){
    return number1/number2;
}

function mod(number1, number2){
    return number1%number2;
}
